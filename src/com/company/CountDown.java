package com.company;

public class CountDown {
    public static void main(String[] args) {

        int first;
        int last;

       int Result;

    }

    public static int Result(int first, int last){
         first = 10;
         last =0;
         while (first==last){
             System.out.println (first);
             first --;

         }
    }
}
